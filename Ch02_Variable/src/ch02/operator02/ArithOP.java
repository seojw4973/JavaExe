package ch02.operator02;

public class ArithOP {

	public static void main(String[] args) {
		// 사칙연산
		int num0 = 10;
		int num1 = 3;
		System.out.println(num0+num1);
		System.out.println(num0-num1);
		System.out.println(num0*num1);
		System.out.println(num0/num1);	// 몫
		System.out.println(num0%num1);	// 나머지
	}
}
